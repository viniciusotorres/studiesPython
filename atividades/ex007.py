primeiraNota = float(input("Digite a nota 1 do aluno: "))
segundaNota = float(input("Digite a nota 2 do aluno: "))
media = (primeiraNota + segundaNota) / 2

print(f"A média do aluno é: {media:.1f}")
print('Primeira nota: {:.1f}'.format(primeiraNota))
print('Segunda nota: {:.1f}'.format(segundaNota))
